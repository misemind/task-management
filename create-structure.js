// Function to create folders
const createFolders = () => {
    folders.forEach(folder => {
      if (!fs.existsSync(`./apps/server/${folder}`)) {
        fs.mkdirSync(`./apps/server/${folder}`, { recursive: true });
        console.log(`Created folder: ${folder}`);
      }
    });
  };
  
  // Function to create files
  const createFiles = () => {
    files.forEach(file => {
      if (!fs.existsSync(`./apps/server/${file}`)) {
        fs.writeFileSync(`./apps/server/${file}`, '', 'utf8');
        console.log(`Created file: ${file}`);
      }
    });
  };
  

// Execute the functions
createFolders();
createFiles();

